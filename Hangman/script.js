const startGame = document.getElementById("start-game");
const submitGuess = document.getElementById("submit-guess");

startGame.addEventListener("click", function () {
  startGame.style.display = "none";
  document.getElementById("game-container").style.display = "flex";

  let randomIndex = Math.floor(Math.random() * randomWords.length);

  let answer = randomWords[randomIndex];

  console.log(answer);

  let answerLetters = answer.split("");

  let displayLetters = "";

  for (let i = 0; i < answerLetters.length; i++) {
    displayLetters +=
      '<span class="letters" id="letter_' +
      i +
      '">' +
      answerLetters[i] +
      "</span>";
  }

  document.getElementById("answer").innerHTML = displayLetters;

  const submitGuess = document.getElementById("submit-guess");

  let wrongGuess = 0;

  let correctGuess = 0;

  let wrongGuessHistory = [];

  let wrongGuessHistroyDisplay = document.getElementById("wrong-guess");

  submitGuess.addEventListener("click", function () {
    let guessLetter = document.getElementById("guess").value;

    if (answer.includes(guessLetter)) {
      console.log(guessLetter);
      for (let i = 0; i < answerLetters.length; i++) {
        if (guessLetter === answerLetters[i]) {
          let changeColor = document.getElementById("letter_" + i);
          changeColor.classList.add("black");
          correctGuess++;
        }
      }
      if (correctGuess === answerLetters.length) {
        document.getElementById("game-game-container").style.display = "none";
        document.getElementById("won").style.display = "flex";
      }
    } else {
      wrongGuess++;
      wrongGuessHistory.push(guessLetter);
      wrongGuessHistroyDisplay.textContent =
        "Wrong Guesses: " + wrongGuessHistory.join(", ");
      if (wrongGuess === 1) {
        document.getElementById("img").src = "./imgs/1.jpg";
      } else if (wrongGuess === 2) {
        document.getElementById("img").src = "./imgs/2.jpg";
      } else if (wrongGuess === 3) {
        document.getElementById("img").src = "./imgs/3.jpg";
      } else if (wrongGuess === 4) {
        document.getElementById("img").src = "./imgs/4.jpg";
      } else if (wrongGuess === 5) {
        document.getElementById("img").src = "./imgs/5.jpg";
      } else if (wrongGuess === 6) {
        document.getElementById("img").src = "./imgs/6.jpg";
        document.getElementById("death").style.display = "flex";
        document.getElementById(
          "death"
        ).textContent = `You Died :(   The Answer was : ${answer}`;
        document.getElementById("answer").style.display = "none";
        document.getElementById("guess").style.display = "none";
        document.getElementById("submit-guess").style.display = "none";
        document.getElementById("wrong-guess").style.display = "none";
      }
    }
    document.getElementById("guess").value = "";
  });
});
